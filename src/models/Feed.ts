import {Schema, model} from 'mongoose'


const feedSchema = new Schema({
    title: String,
    message: String,
    creator: String,
    tags: [String],
    selectedFile: String,
    likeCount: {
        type: Number,
        default: 0,
    },
    createdAt: {
        type: Date,
        default: new Date(),
    },
});

const Feed = model('Feed', feedSchema);
export default Feed;